
namespace TXCommons.PictureModular
{
    public class KeyManager
    {
        public static string GetPremisesPictureKey(string Guid, string PictureType)
        {
            return ("Premises:" + PictureType.ToUpper() + ":" + Guid);
        }

        public static string GetUserPictureKey(string Guid, string PictureType)
        {
            return ("User:" + PictureType.ToUpper() + ":" + Guid);
        }

        public static string GetDocumentKey(string Guid, string DocumentType)
        {
            return ("Document:" + DocumentType + ":" + Guid);
        }
    }
}
