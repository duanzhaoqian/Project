using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace KYJ.ZS.Models.DB
{
    /// <summary>
    /// 作者：wwang
    /// 时间：2014-5-7
    /// 描述：品牌实体类
    /// </summary>
    public class Brand
    {
        /// <summary>
        /// 品牌ID
        /// </summary>
        public int Id { get; set; }

        /// <summary>
        /// 品牌名称
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// 品牌图片GUID
        /// </summary>
        public string Guid { get; set; }

        /// <summary>
        /// 品牌状态：0 未知，1 待审核，2 审核通过，3 审核未通过
        /// </summary>
        public int State { get; set; }

        /// <summary>
        /// 品牌排序
        /// </summary>
        public int Sort { get; set; }

        /// <summary>
        /// 创建时间
        /// </summary>
        public DateTime CreateTime { get; set; }

        /// <summary>
        /// 更新时间
        /// </summary>
        public DateTime UpdateTime { get; set; }

        /// <summary>
        /// 是否品牌中心显示
        /// </summary>
        public bool IsBrandCenter { get; set; }

        /// <summary>
        /// 是否导航
        /// </summary>
        public bool IsNavigation { get; set; }

        /// <summary>
        /// 是否删除
        /// </summary>
        public bool IsDel { get; set; }
    }
}
